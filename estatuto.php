                <h1 class="h2 text-center" id="estatuto">Associação Atlética Ferroviária<br>Estatuto Social</h1>
                <p class="text-center">Pindamonhangaba, <time datetime="2008-12-09">09 de dezembro de 2008</time>.</p>
                <hr class="my-4">
                <h4 class="text-center mb-3" id="item-1">Capítulo I: Da Fundação – Denominação, Sede e Fins</h4>
                <p id="item-1-1"><b class="h5">Art.1° - </b>Com a denominação de Associação Atlética Ferroviária de Pindamonhangaba, fundada em 12 de abril de 1930, pelos ferroviários da Estrada de Ferro Campos do Jordão e Estrada de Ferro Central do Brasil, uma agremiação social e desportiva com CNPJ 54.125.919/0001-30, com Sede à Rua Álvaro Pinto Madureira, 81 – CEP 12.422-210 –  Foro de Pindamonhangaba – SP.</p>
                <p id="item-1-2"><b class="h5">Art.2° - </b>A Associação, com número ilimitado de associados, funcionários ou não das referidas Estradas de Ferro, é uma associação de direito privado e com personalidade jurídica distinta da de seus associados e de Utilidade Pública, com tempo de duração indeterminado.</p>
                <p id="item-1-3"><b class="h5">Art.3° - </b>A Associação tem por finalidade difundir entre os seus associados a prática dos desportos em geral e promover reuniões sociais, esportivas, recreativas e culturais.</p>
                <p id="item-1-4"><b class="h5">Art.4° - </b>Para integral realização dos seus fins e objetivos, a Associação deve manter instalações sociais e desportivas, filiar-se às federações desportivas e participar ou não dos campeonatos e demais competições organizadas pelas entidades a que estiver filiada.</p>
                <p id="item-1-5"><b class="h5">Parágrafo Único - </b>Para a manutenção da Associação existirão as seguintes fontes de recursos:</p>
                <ol type="a">
                    <li>Taxa de manutenção do associado titular</li>
                    <li>Taxa de manutenção do dependente do associado titular</li>
                    <li>Comercialização de títulos e/ou taxas de jóia</li>
                    <li>Aluguéis de suas dependências</li>
                    <li>Doações</li>
                    <li>Comercialização de veiculação de propaganda de terceiros</li>
                    <li>Outras fontes de recursos que vierem a ser instituídas</li>
                </ol>
                <h4 class="text-center my-3" id="item-2">Capítulo II: Dos Associados</h4>
                <p id="item-2-1"><b class="h5">Art.5° - </b>O quadro social será composto de associados ferroviários e associados não ferroviários, com iguais direitos e deveres, sem distinção de classe, raça, cor, sexo e religião, divididos nas seguintes categorias:</p>
                <ol type="a">
                    <li>Contribuintes (Título Expansão)</li>
                    <li>Contribuintes não titulados e Ferroviários</li>
                    <li>Grande Patrono</li>
                    <li>Remidos</li>
                    <li>Beneméritos</li>
                    <li>Honorários</li>
                    <li>Temporários</li>
                    <li>Militantes</li>
                </ol>
                <p id="item-2-2"><b class="h5">Parágrafo único - </b>Mediante o pagamento da taxa de manutenção em dobro e pelo prazo máximo de 03 (três) meses, poderão ser admitidos associados temporários, desde que o interessado não resida em nossa cidade e que seja apresentado por um associado. Os associados temporários não terão acesso às dependências da Associação nos bailes, shows e carnaval.</p>
                <p id="item-2-3"><b class="h5">Art.6° - </b>São contribuintes titulados os associados portadores de títulos de expansão social ou grande patrono e contribuintes não titulados e ferroviários (a) os associados admitidos com o pagamento da taxa de jóia.</p>
                <p id="item-2-4"><b class="h5">Art.7° - </b>O título de associado Remido será concedido pelo Conselho Deliberativo, ao associado que pelo seu trabalho e dedicação, fizer jus a essa homenagem, obedecidas as determinações do artigo 30° deste Estatuto.</p>
                <p id="item-2-5"><b class="h5">Art.8° - </b>São Beneméritos os associados que, por relevantes serviços prestados à Associação, tiveram esse título concedido pelo Conselho Deliberativo.</p>
                <p id="item-2-6"><b class="h5">Art.9° - </b>São associados Honorários aqueles que, embora não fazendo parte do quadro social da Associação, tiveram esse título concedido pelo Conselho Deliberativo, por relevantes serviços prestados à Associação em competições esportivas.</p>
                <p id="item-2-7"><b class="h5">Art.10° - </b>São Militantes aqueles que, por suas aptidões esportivas sejam selecionados pela Diretoria para representar a Associação em competições esportivas.</p>
                <p id="item-2-8"><b class="h5">Parágrafo primeiro - </b>Para poderem freqüentar as diversas dependências mantidas pela Associação, os Militantes e seus dependentes receberão um cartão de identidade com validade somente durante o período em que estiverem vinculados esportivamente à Associação.</p>
                <p id="item-2-9"><b class="h5">Parágrafo segundo - </b>Quando o militante já for associado, ficará isento da taxa de manutenção durante o período que durar a competição.</p>
                <p id="item-2-10"><b class="h5">Art.11° - </b>No caso de falecimento de associado contribuinte não titulado o seu registro no quadro social será transferido, sem qualquer despesa, para sua viúva e, na falta desta os seus dependentes poderão, dentro do prazo de 06 (seis) meses, ingressar no quadro social com isenção da taxa de jóia.</p>
                <p id="item-2-11"><b class="h5">Art.12° - </b>Para ingressar no quadro social o candidato deve cumprir os seguintes requisitos:</p>
                <ol type="a">
                    <li>ser proposto por um  associado maior de 18 anos.</li>
                    <li>gozar de bom conceito.</li>
                    <li>não exercer nem ter exercido atividades que atentem a moral e aos bons costumes.</li>
                    <li>apresentar atestado de sanidade física e mental.</li>
                    <li>se for menor de 18 (dezoito) anos, juntar à proposta autorização do pai ou responsável legal com firma reconhecida.</li>
                    <li>pagar a taxa de jóia, ou adquirir um título.</li>
                </ol>
                <p id="item-2-12"><b class="h5">Parágrafo único - </b>A Comissão de Sindicância poderá, se julgar necessário, solicitar atestado de antecedentes criminais.</p>
                <p id="item-2-13"><b class="h5">Art.13° - </b>O associado pode inscrever como seu dependente:</p>
                <ol type="a">
                    <li>seu cônjuge ou companheira (o) devidamente comprovada através de declaração atestada por 02 (dois) associados, devendo ser renovada anualmente.</li>
                    <li>seus filhos ou tutelados, menores de 18 (dezoito) anos.</li>
                    <li>suas filhas ou tuteladas, solteiras ou separadas judicialmente, com comprovação de dependência econômica.</li>
                    <li>sua mãe, seu pai, sogro ou sogra, viúvo(a) ou separado (a) judicialmente, com apresentação de declaração do sócio titular e de dependência econômica.</li>
                    <li>menores de 18 (dezoito) anos, que estejam sob sua guarda, mediante a apresentação do Termo de Guarda do Juizado de Menores e excepcionalmente com a apresentação de declaração de dependência econômica e financeira devidamente registrada em cartório.</li>
                    <li>sua enteada (o), se menor de 18 (dezoito) anos apresentar declaração de dependência econômica financeira, para julgamento da Comissão de Sindicância</li>
                    <li>filho (a), tutelado (a), com qualquer idade, inscrever e manter dependente quando portadores de necessidades especiais</li>
                    <li>Militantes</li>
                </ol>
                <p id="item-2-14"><b class="h5">Parágrafo único - </b>O associado pode inscrever neto (a) menores de 18 anos – sendo o 1º no valor normal estipulado pelo Conselho Deliberativo, e os subseqüentes em dobro.</p>
                <p id="item-2-15"><b class="h5">Art.14° - </b>Os associados pagarão para os dependentes a taxa mensal que será fixada pelo Conselho Deliberativo.</p>
                <p id="item-2-16"><b class="h5">Art.15° - </b>Os filhos e os tutelados ao completarem 18 (dezoito) anos, poderão, dentro do prazo de 06 (seis) meses, ingressar no quadro social, com isenção de taxa de jóia. Neste período poderão freqüentar as dependências da Associação, desde que pague a taxa de manutenção como dependente.</p>
                <p id="item-2-17"><b class="h5">Parágrafo primeiro - </b>A Diretoria, a pedido do associado, poderá isentar do pagamento da taxa de jóia, suas filhas ou tuteladas solteiras ou separadas judicialmente, desde que estejam inscritas como dependentes.</p>
                <p id="item-2-18"><b class="h5">Parágrafo segundo - </b>Os agraciados associados Beneméritos, Honorários e Remidos e seus dependentes estão isentos da taxa de manutenção.</p>
                <p id="item-2-19"><b class="h5">Art.16° - </b>As propostas para associados, os pedidos para transferência de títulos e os pedidos para a inscrição de dependentes, só terão andamento depois de aprovados pela Comissão de Sindicância.</p>
                <p id="item-2-20"><b class="h5">Parágrafo único - </b>As propostas para admissão de associados não aprovadas pela Comissão de Sindicância, deverão ser encaminhadas a Diretoria, cabendo a esta, em votação secreta, aprovar ou não a proposta.</p>
                <p id="item-2-21"><b class="h5">Art.17° - </b>Os direitos e deveres dos associados Honorários, dos associados Temporários, dos Atletas Militantes e dos dependentes, são estipulados pelo item “a” do artigo 21° e pelos itens “b” “c” “d” “e” “f” “g” “h” e “j” do artigo 27°.</p>
                <p id="item-2-22"><b class="h5">Art.18° - </b>A aquisição de títulos não confere nenhum direito se a proposta não for aprovada pela Comissão de Sindicância e Diretoria.</p>
                <p id="item-2-23"><b class="h5">Art.19° - </b>O título de Grande Patrono, a pedido do seu portador pode ser substituído por dois títulos Expansão.</p>
                <p id="item-2-24"><b class="h5">Art.20° - </b>Os associados jamais responderão, solidária ou subsidiariamente, pelas obrigações de qualquer natureza contraídas pela Diretoria em nome da Associação.</p>
                <p id="item-2-25"><b class="h5">Art.21° - </b>São direitos dos associados:</p>
                <ol type="a">
                    <li>freqüentar as diversas dependências mantidas pela Associação nos horários estipulados pela Diretoria e usufruir de tudo que estiver  a disposição  dos associados;</li>
                    <li>participar  das Assembléias, se estiver em gozo de todos os direitos estatutários, votar se for maior de 18 (dezoito) anos e ter sido admitido no quadro social há mais de 01 (um) ano, ser votado se for maior de 18 (dezoito) anos e  ter  sido admitido no quadro social há mais de 04 (quatro) anos;</li>
                    <li>recorrer, dentro do prazo de 15 (quinze) dias, à Diretoria, nos casos de advertência ou suspensão e ao Conselho Deliberativo nos casos de exclusão;</li>
                    <li>trazer pessoas amigas para conhecerem as instalações sociais e esportivas da Associação, registrando em livro próprio na secretaria;</li>
                    <li>reclamar, por escrito, de toda e qualquer irregularidade que venha constatar, sugerindo providências;</li>
                    <li>transferir  seu titulo ou desmembrar o título Grande Patrono;</li>
                    <li>requerer, por escrito, com apoio de, no mínimo, 100  (cem)  associados com direito a voto, reunião extraordinária do Conselho Deliberativo, devendo constar do requerimento o motivo do pedido;</li>
                    <li>requerer por escrito, com apoio de, no mínimo,  1/5 (um quinto)  de associados com direito a voto, a convocação de uma  Assembléia Geral  Extraordinária, para tratar de qualquer assunto de interesse da Associação;</li>
                    <li>registrar, com apoio de, no mínimo, 04 (quatro) membros efetivos do Conselho Deliberativo, chapas completas para concorrerem às eleições do referido Conselho Deliberativo, desde que sejam obedecidas todas as exigências estatutárias;</li>
                    <li>registrar, com apoio de, no mínimo, 100 (cem) associados com direito a voto ou de 04 (quatro) membros efetivos do Conselho Deliberativo, chapas para concorrerem ás eleições para,  Membros do Conselho Deliberativo e seus suplentes, Presidente e Vice-Presidente da Diretoria, Membros do Conselho Fiscal e seus suplentes, Membros da Comissão de Sindicância e seus suplentes;</li>
                    <li>obter licença com isenção de taxa de manutenção quando mudar de município, voltando a pagar quando retornar, ficando impedido a sua freqüência enquanto perdurar sua licença;</li>
                </ol>
                <p id="item-2-26"><b class="h5">Art.22° - </b>É proibido o voto por procuração em qualquer das eleições realizadas pela Associação, seja qual for a forma de mandato.</p>
                <p id="item-2-27"><b class="h5">Art.23° - </b>Serão cancelados, mediante aviso, os associados contribuintes titulados ou não titulados, que deixarem de pagar a taxa de manutenção 12 (doze) meses consecutivos.</p>
                <p id="item-2-28"><b class="h5">Art.24° - </b>Os associados titulados cancelados serão reabilitados se pagarem as mensalidades atrasadas pelo valor do dia com 10% de multa.</p>
                <p id="item-2-29"><b class="h5">Art.25° - </b>Os associados não titulados cancelados poderão ser readmitidos se cumprirem as exigências do Artigo 12° deste Estatuto e se a proposta for aprovada pela Diretoria.</p>
                <p id="item-2-30"><b class="h5">Art.26° - </b>Os títulos só serão transferidos se seus portadores estiverem em dia com o pagamento da taxa de manutenção.</p>
                <p id="item-2-31"><b class="h5">Art.27° - </b>São deveres dos associados(as):</p>
                <ol type="a">
                    <li>pagar em dia a taxa de manutenção, e outras que vierem a ser instituídas, e honrar as demais obrigações contraídas assim como, Plano de Saúde e outros convênios  oferecidos pela Associação, entre outras obrigações;</li>
                    <li>cumprir as determinações do Estatuto Social, do Regimento Interno e as resoluções da Diretoria;</li>
                    <li>exibir a sua carteira social e o recibo da taxa de manutenção para provar que está em dia, sempre que solicitada, sendo esta a condição indispensável para o seu ingresso em qualquer dependência da Associação;</li>
                    <li>comunicar à secretaria quando mudar o endereço;</li>
                    <li>zelar pelo patrimônio da Associação;</li>
                    <li>indenizar  os prejuízos materiais e financeiros que causar à Associação, inclusive os causados pelos seus dependentes e convidados;</li>
                    <li>tratar todos com cordialidade e respeito;</li>
                    <li>quando nas dependências da Associação, abster-se de manifestações de caráter político, religioso ou de classe;</li>
                    <li>participar das Assembléias, se estiver em gozo de todos os direitos estatutários, comunicar à Diretoria, registrando em livro  próprio, toda e qualquer irregularidade  que venha a constatar;</li>
                    <li>comunicar à diretoria registrando em livro próprio toda e qualquer irregularidade que venha a constatar;</li>
                </ol>
                <p id="item-2-32"><b class="h5">Art.28° - </b>A não indenização dos prejuízos materiais e financeiros causados à Associação  priva o associado e seus dependentes de todos os seus direitos estatutários e a indenização não o exime de outras penalidades, se cabíveis.</p>
                <p id="item-2-33"><b class="h5">Art.29° - </b>Os títulos de Grande Patrono ou Expansão Social, no caso de falecimento do seu portador, serão transferidos sem qualquer despesa para sua viúva e, na falta desta, ao herdeiro indicado pelo Inventário.</p>
                <p id="item-2-34"><b class="h5">Art.30° - </b>Os títulos de Associado Benemérito, de Associado Honorário, de Associado Remido e Honra ao Mérito nos casos previstos pelo Artigo 7° do Estatuto Social, só serão concedidos se a proposta for aprovada, em votação secreta, por 50% (cinqüenta por cento) mais um dos Conselheiros presentes à reunião.</p>
                <p id="item-2-35"><b class="h5">Art.31° - </b>Somente o Presidente da Diretoria e os membros do Conselho Deliberativo, poderão apresentar proposta para concessão dos títulos Beneméritos, Honorários, Remidos e Honra ao Mérito que se trata o artigo 30° deste Estatuto.</p>
                <p id="item-2-36"><b class="h5">Art.32° - </b>O associado que recebeu o título de Associado Benemérito ou Associado Remido, pode transferir seu título de Grande Patrono ou de Expansão Social.</p>
                <p id="item-2-37"><b class="h5">Art.33° - </b>O pagamento da taxa de jóia, da taxa de transferência e das mensalidades atrasadas, poderá ser parcelado a critério do Presidente da Diretoria.</p>
                <p id="item-2-38"><b class="h5">Parágrafo único - </b>As mensalidades em atraso, deverão ser recebidas com 10% (dez por cento) ao mês da multa, proporcional aos dias de atraso.</p>
                <p id="item-2-39"><b class="h5">Art.34° - </b>Para poder freqüentar qualquer das dependências da Associação, o associado deve estar em dia com o pagamento da taxa de manutenção e em pleno gozo dos seus direitos estatutários.</p>
                <p id="item-2-40"><b class="h5">Art.35° - </b>O associado excluído do quadro social pode, depois de decorridos 02 (dois) anos da data de sua eliminação, solicitar ao Conselho Deliberativo a sua readmissão, cabendo a este, em votação secreta, atender ou não o pedido.</p>
                <p id="item-2-41"><b class="h5">Art.36° - </b>Os associados são passíveis das seguintes penalidades:</p>
                <ol type="a">
                    <li>advertência por escrito.</li>
                    <li>suspensão até 06 (seis) meses.</li>
                    <li>eliminação.</li>
                </ol>
                <p id="item-2-42"><b class="h5">Parágrafo primeiro - </b>No caso de suspensão de um associado, tal fato não abrangerá a qualquer de seus dependentes.</p>
                <p id="item-2-43"><b class="h5">Parágrafo segundo - </b>No caso de eliminação de um associado, tal fato abrangerá todos os seus dependentes.</p>
                <p id="item-2-44"><b class="h5">Parágrafo terceiro - </b>No caso de eliminação de um dependente, a penalidade não se estenderá ao associado e aos demais dependentes.</p>
                <p id="item-2-45"><b class="h5">Art.37° - </b>Cabe à Diretoria aplicar as penalidades de advertência, suspensão e exclusão, com base no parecer da Comissão de Justiça Disciplinar.</p>
                <p id="item-2-46"><b class="h5">Art.38° - </b>As penalidades de suspensão e exclusão serão aplicadas quando o associado:</p>
                <ol type="a">
                    <li>for reincidente.</li>
                    <li>atentar contra a disciplina.</li>
                    <li>atentar, por ação ou omissão, contra o conceito público da Associação.</li>
                    <li>fazer declaração falsa ou de má fé para ingressar no quadro social ou para inscrever dependentes.</li>
                    <li>ceder sua carteira social e ou qualquer autorização, a outra pessoa, para que esta possa ingressar em qualquer das dependências da Associação.</li>
                    <li>não tratar com cordialidade e respeito os diretores da Associação ou seus prepostos.</li>
                    <li>comportar-se de maneira inconveniente em qualquer das dependências da Associação.</li>
                    <li>promover discórdia entre os associados.</li>
                    <li>aplicar agressão física, dentro de qualquer das dependências da Associação</li>
                    <li>passar a exercer atividades ilícitas</li>
                    <li>for condenado por sentença transitada em julgado, pela prática de delito infamante.</li>
                    <li>portar qualquer tipo de entorpecentes ou qualquer tipo de arma.</li>
                </ol>
                <p id="item-2-46"><b class="h5">Art.39° - </b>Ao associado deve ser garantido o seu direito de defesa e, na aplicação da penalidade, devem ser consideradas, a gravidade da falta, a repercussão e o procedimento anterior do associado.</p>
